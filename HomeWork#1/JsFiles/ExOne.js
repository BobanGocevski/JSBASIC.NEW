console.log("type of number:"); // so ovoj kod pecatime tekst linija kako "naslov" za sledniot kod.
console.log(typeof(number)); //  go proveruvame tipot na  promelnivata "number" pokazuva koj tip sme iskoristile.

var string = "10"; //ova e string zatoa sto koristime ""
console.log(string);
console.log("type of string:")
console.log(typeof(string));

var boolean = true; // ovam moze da bide i false, vo boolean kako vrednost koristime samo "true" i "false" bez ""
